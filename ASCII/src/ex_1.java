public class ex_1 {
    public static void main(String[] args) {

        /*
         * Ascii - 	Stands for American Standard code
         * 			for Information Interchange
         *
         * 			A - Z = 65 - 90
         * 			a - z = 97 - 122
         * 			Space = 32
         * 			0 - 9 = 48 - 57
         * */

        for (int i = 48; i <= 57 ; i++) {
            System.out.println(i + " = " + (char)i);
        }

    }
}
